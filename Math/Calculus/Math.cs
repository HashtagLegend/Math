using System;
using System.Collections.Generic;
using System.Text;

namespace Math
{
    public class Math
    {
        /// <summary>
        /// adds 2 int values: a + b
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public int Addition(int a, int b)
        {
            return a + b;
        }
        /// <summary>
        /// Subtracts 2 int values: a - b
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public int Subtraction(int a, int b)
        {
            return a - b;
        }
        /// <summary>
        /// Divides 2 values: a / b
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public int Division(int a, int b)
        {
            return a / b;
        }
        /// <summary>
        /// Multiplies 2 int values: a * b
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public int Multiply(int a, int b)
        {
            return a * b;
        }
        
    }
}
